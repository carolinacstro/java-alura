
public class TestaVariaveis {

	public static void main(String[] args) {
		System.out.println("Teste de variáveis");
		
		int idade;
		idade = 23;
		System.out.println(idade);
		
		idade = 30 + 10;
		System.out.println(idade);
		
		idade = (5 * 5) + 2;
		System.out.println(idade);
		
		System.out.println("a idade é " + idade + ", parabéns!");
	}
}
